// integer function

#include <iostream>
using namespace std;
 
int main () {
  int myNum = 1000;
  cout << myNum;
  return 0;
}


// float function

#include <iostream>
using namespace std;
 
int main () {
  float myNum = 5.75;
  cout << myNum;
  cout << "\nThis will give float function";
  return 0;
}

// double function

#include <iostream>
using namespace std;
 
int main () {
  double myNum = 19.99;
  cout << myNum;
  cout << "\nThis will give double function";
  return 0;
}

// scientific number values

#include <iostream>
using namespace std;
 
int main () {
  float f1 = 35e3;
  double d1 = 12E4;
  cout << f1 << "\n";
  cout << d1;
  return 0;
}