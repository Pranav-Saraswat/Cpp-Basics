// Real Life Example of boolean Expression

#include <iostream>
using namespace std;

int main() {
  int myAge = 25;
  int votingAge = 18;
  
  cout << (myAge >= votingAge);  // returns 1 (true), meaning 25 year olds are allowed to vote!
  return 0;
}

